
public class SwitchWithStrings {

	public static void main(String[] args) {

		String value = "Januari";
		
		switch (value) {
		case "Januari":
			System.out.println("The value is 1");
			break;
		case "Februari":
			System.out.println("The value is 2");
			break;
		case "March":
			System.out.println("The value is 3");
			break;

		default:
			break;
		}

	}

}
